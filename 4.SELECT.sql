-- 제품 테이블에서 한빛제과가 제조한 제품의 제품명, 재고량, 단가를 검색하시오.

SELECT 제품명,재고량,단가
from 제품
where 제조업체 = '한빛제과';

--주문 테이블에서 apple 고객이 15개 이상 주문한 주문제품,수량,주문일자를 검색하시오.
SELECT 주문제품,수량,주문일자
from 주문
where 주문고객 = 'apple' AND 수량 >= 15;

--주문 테이블에서 apple고객이 주문했거나 15개 이상 주문된 제품의 주문제품,수량,주문일자,주문고객을 검색하시오.
select 주문제품,수량,주문일자,주문고객
from 주문
where 주문고객 = 'apple' or 수량 >= 15;

/*
    제품 테이블에서 단가가 2,000원 이상이면서 3,000원 이하인 제품의
    제품명,단가,제조업체를 검색하시오.
*/
select 제품명,단가,제조업체
from 제품
where 단가 >= 2000 and 단가 <= 3000;


-- 고객 테이블에서 성이 김 씨인 고객의 고객이름, 나이, 등급, 적립금을 검색하시오.
